#!/usr/bin/python3
"""
    3-is_kind_of_class.py: returns True if the object is an instance of,
    or if the object is an instance of a class
"""


def is_kind_of_class(obj, a_class):
    """
        Check if obj is a instance or if obj
        in a instance of a class
    """

    return isinstance(obj, a_class)
